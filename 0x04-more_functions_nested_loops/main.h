/**
 * _isupper - check if the given character is Upper case or lower case
 * @c: the input parameter
 * Return: 1 if the given char is upper and 0 otherwise
 */
int _isupper(int c);
/**
 * _isdigit - check if the given character is digit or not
 * @c: the input parameter
 * Return: 1 if the given input is digit and 0 otherwise
 */
int _isdigit(int c);
/**
 * mul - return the multiplication of the given two values
 * @a: the first input
 * @b: the second input
 * Return: return the multiplication of the given two values
 */
int mul(int a, int b);
